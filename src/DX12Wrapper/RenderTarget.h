#pragma once

#include "../Utility/EngineUtility.h"

#include "Texture.h"
#include "DescriptorHeapCBV_SRV_UAV.h"
#include "RenderTargetBuffer.h"
#include "DepthStencilBuffer.h"
#include "DescriptorHeapRTV.h"
#include "DescriptorHeapDSV.h"

namespace DX12Wrapper
{
	class RenderingContext;

	struct RenderTargetData
	{
		RenderTargetBufferData renderTargetBufferData;	// レンダーターゲットバッファー用データ
		DepthStencilBufferData depthStencilBufferData;	// デプスステンシルバッファー用データ
		bool useDepth;
		bool isCubeMap;
	};

	/// <summary>
	/// レンダーターゲットクラス
	/// </summary>
	class RenderTarget
	{
	private:
		Texture m_renderTargetTexture;				// オフスクリーンテクスチャ
		DescriptorHeapCBV_SRV_UAV m_textureHeap;		// オフスクリーンテクスチャ用ヒープ

		RenderTargetBuffer m_renderTargetBuffer;		// レンダーターゲットバッファー
		DescriptorHeapRTV m_rtvHeap;					// レンダーターゲット用ヒープ

		Texture m_depthStencilTexture;				// デプスステンシルテクスチャ
		DepthStencilBuffer m_depthStencilBuffer;		// デプスステンシルバッファー
		DescriptorHeapDSV m_dsvHeap;					// デプスステンシル用ヒープ

		RenderTargetData m_renderTargetData;			// レンダーターゲットデータ

	public:
		/// <summary>
		/// レンダーターゲット生成
		/// </summary>
		/// <param name="device">デバイス</param>
		/// <returns></returns>
		Utility::RESULT Create(ID3D12Device& device, RenderTargetData& renderTargetData);

		/// <summary>
		/// レンダリング開始
		/// </summary>
		/// <param name="renderContext">レンダリングコンテキスト</param>
		void BeginRendering(RenderingContext& renderContext, CD3DX12_VIEWPORT& viewport, CD3DX12_RECT& scissorRect);

		/// <summary>
		/// レンダリング終了
		/// </summary>
		/// <param name="renderContext">レンダリングコンテキスト</param>
		void EndRendering(RenderingContext& renderContext);

		/// <summary>
		/// レンダーターゲットのテクスチャ取得
		/// </summary>
		/// <returns>レンダーターゲットテクスチャ</returns>
		Texture& GetRenderTargetTexture() {
			return m_renderTargetTexture;
		}

		/// <summary>
		/// デプスステンシルのテクスチャ取得
		/// </summary>
		/// <returns>デプスステンシルテクスチャ</returns>
		Texture& GetDepthStencilTexture() {
			return m_depthStencilTexture;
		}

		/// <summary>
		/// マルチレンダーターゲットでのレンダリング開始
		/// </summary>
		/// <param name="renderTargets"></param>
		/// <param name="renderContext"></param>
		/// <param name="viewport"></param>
		/// <param name="scissorRect"></param>
		static void BeginMultiRendering(
			RenderTarget* renderTargets, const size_t& length, RenderingContext& renderContext,
			CD3DX12_VIEWPORT& viewport, CD3DX12_RECT& scissorRect);

		/// <summary>
		/// マルチレンダーターゲットでのレンダリング終了
		/// </summary>
		/// <param name="renderTargets"></param>
		/// <param name="length"></param>
		/// <param name="renderContext"></param>
		static void EndMultiRendering(
			RenderTarget* renderTargets, const size_t& length, RenderingContext& renderContext);
	};
}